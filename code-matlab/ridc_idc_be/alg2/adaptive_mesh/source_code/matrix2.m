
function rh=matrix(p)
 % RIDC2
 
   if p==2 
       
      s=zeros(p-1,p);
            
      s(1,1)=0.5;
      s(1,2)=0.5;

   elseif p==3 
       
% RIDC3 

     s=zeros(p-1,p);

     s(1,1)=5/12;
     s(1,2)=2/3;
     s(1,3)=-1/12;
     s(2,1)=-1/12;
     s(2,2)=2/3;
     s(2,3)=5/12;

   elseif p==4 
       
% RIDC4
      
     s=zeros(p-1,p);

     s(1,1)=3/8;
     s(1,2)=19/24;
     s(1,3)=-5/24;
     s(1,4)=1/24;

     s(2,1)=-1/24;
     s(2,2)=13/24;
     s(2,3)=13/24;
     s(2,4)=-1/24;

     s(3,1)=1/24;
     s(3,2)=-5/24;
     s(3,3)=19/24;
     s(3,4)=3/8;
     
   end
     
   rh=s;

end